# Operadores

# Operadores Aritmeticos
print(3 + 4) # Suma
print(3 - 4) # Resta
print(3 * 4) # Multiplicacion
print(3 / 4) # Division
print(10 % 3) # Modulo
print(10 // 3) # Division Entera
print(2 ** 3) # Potencia

print('Hola ' + 'Python') 
print('Hola ' + str(5))
print('Hola ' * 5)
print('Hola ' * (2 ** 3))

my_float = 2.5 * 2
print('Hola ' * int(my_float))

print('---------------------------------------------------------------')

# Operadores Comparativos
print(3 > 4) # Mayor que
print(3 < 4) # Menor que
print(3 >= 4) # Mayor o Igual que
print(3 <= 4) # Menor o Igual que
print(3 == 4) # Igual que
print(3 != 4) # Dieferente de

print('Hola' > 'Python')
print('Hola' < 'Python')
print('Hola' >= 'Zola') # Ordenación Alfabeticca
print('Hola' <= 'Python')
print('Hola' == 'Hola')
print('Hola' != 'Python')

print('---------------------------------------------------------------')

# Operadores Logicos
print(3 > 4 and 'Hola' > 'Python')
print(3 > 4 or 'Hola' > 'Python')
print(3 < 4 and 'Hola' < 'Python')
print(3 < 4 or ('Hola' < 'Python' and 4 == 4))
print(not 'Hola' > 'Python')
print(not 'Hola' < 'Python')
print(not(3 < 4))
